package 기본2_문제_산술;

public class 기본2_문제01_여학생수_정답 {

	public static void main(String[] args) {
		/* 
		[문제]	  	
			철수네 반 학생은 총 31명이다. 
			남학생이 17명이면 여학생은 몇 명인지 구하시오.			
		[정답] 
			14
		*/	
	
		/*
			[풀이]
				남학생 + 여학생 = 31명
				17 	   + n      = 31
				n = 31 - 17
		*/
		System.out.println(31 - 17);	

	}

}
